// 공통 회사 컨텍스트는 입력 산출물 기반 단일 출처(lib/knowledge/company.ts)에서 가져온다.
// 여기서 re-export 해 기존 import 경로(@/lib/personas의 COMPANY_CONTEXT)도 그대로 동작하게 둔다.
